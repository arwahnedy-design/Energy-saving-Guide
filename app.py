st.set_page_config(page_title="دليل ترشيد الطاقة الذكي", page_icon="💡", layout="centered")

# 2. العنوان الرئيسي والترحيب
st.title("💡 دليل ترشيد استهلاك الطاقة الرقمي")
st.markdown("---")
st.subheader("أهلاً بك في دليلك الذكي لتوفير الكهرباء")
st.write("هدفنا نساعدك تفهم استهلاكك وتقلل فاتورتك بخطوات بسيطة.")

# --- القسم الأول: الحاسبة التفاعلية (أول حاجة تظهر للمستخدم) ---
st.header("🔢 احسب وفرك الشهري")
with st.expander("اضغط هنا لفتح الحاسبة التفاعلية 👇", expanded=True):
    col1, col2 = st.columns(2)
    
    with col1:
        options = ["تكييف", "سخان كهربائي", "غلاية مياه (كاتل)", "إضاءة عادية"]
        device_type = st.selectbox("اختار أكثر جهاز بتستخدمه:", options)
        electricity_price = 1.5  # سعر الكيلوواط الافتراضي
        st.write(f"سعر الكيلوواط المعتمد: {electricity_price} ج.م")
        
    with col2:
        hours = st.slider("ساعات التشغيل اليومية:", 1, 24, 5)
        
    # معادلة الحساب المبنية على قدرة الأجهزة المتوسطة
    power_map = {"تكييف": 2.5, "سخان كهربائي": 2.0, "غلاية مياه (كاتل)": 1.5, "إضاءة عادية": 0.1}
    consumption = hours * power_map[device_type] * 30
    monthly_cost = consumption * electricity_price
    
    if st.button("احسب الآن"):
        st.success(f"تكلفة استهلاك هذا الجهاز شهرياً: {monthly_cost:.2f} ج.م")
        potential_savings = monthly_cost * 0.25
        st.info(f"💡 باتباع نصائح الترشيد، تقدر توفر حوالي {potential_savings:.2f} جنيه شهرياً!")

st.markdown("---")

